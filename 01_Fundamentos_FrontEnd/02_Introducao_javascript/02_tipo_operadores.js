// operadores aritimeticos

let numero_varivel = 10;

console.log('Valor da variável:', numero_varivel)

// Adição
numero_varivel += 10;
console.log('Valor da variável:', numero_varivel)

// Subtração
numero_varivel -= 10;
console.log('Valor da variável:', numero_varivel)

// Multiplicação
numero_varivel *= 5;
console.log('Valor da variável:', numero_varivel)

// Divisão
numero_varivel /= 2;
console.log('Valor da variável:', numero_varivel)